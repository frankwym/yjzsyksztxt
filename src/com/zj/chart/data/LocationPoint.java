package com.zj.chart.data;

/**
 * 
 * @author chen
 * 
 * 定位点X 、 Y
 *
 */
public class LocationPoint {
	private double X;
	private double Y;
	
	public double getX() {
		return this.X;	
	}
	public double getY() {
		return this.Y;	
	}
	
	public void setX(double X) {
		this.X = X;
	}
	public void setY(double Y) {
		this.Y = Y;
	}
}
