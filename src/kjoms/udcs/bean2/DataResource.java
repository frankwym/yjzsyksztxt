package kjoms.udcs.bean2;

/*
 * 基础统计的视图的基础统计表
 */
public class DataResource {
	private String name;
	private String id;
	private String url;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
